// User function template for C++
class Solution {
  public:
    int countPairsWithDiffK(vector<int>& arr, int k) {
        // code written by sunny
        int p = 0;
        unordered_map<int,int> mp;
        for(int x:arr)mp[x]++;
        for(int x:arr)p+=mp[x+k];
        return p;
    }
};

